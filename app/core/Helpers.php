<?php

class Helpers
{
    public static $months = [
        'January' => 'Januari',
        'February' => 'Februari',
        'March' => 'Maret',
        'April' => 'April',
        'May' => 'Mei',
        'June' => 'Juni',
        'July' => 'Juli',
        'August' => 'Agustus',
        'September' => 'September',
        'October' => 'Oktober',
        'November' => 'November',
        'December' => 'Desember'
    ];

    public static function getIndonesianMonth($englishMonth)
    {
        return self::$months[$englishMonth] ?? $englishMonth;
    }
}
